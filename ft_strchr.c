#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*i;
	char	cc;

	cc = (char)c;
	i = (char *)s;
	while (*i != cc && *i != '\0')
		i++;
	if (*i != '\0' || cc == '\0')
		return (i);
	return (0);
}
